    private void actualizarTablaInventario() {
    DefaultTableModel model = (DefaultTableModel) tablaInventario.getModel();
    model.setRowCount(0); // Limpiar la tabla

    for (Inventario.Producto c : proD.obtenerTodos()) {
        model.addRow(new Object[]{c.getCodigo(), c.getNombre(), c.getStock(), c.getPrecio()});
    }
}
