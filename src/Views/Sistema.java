    private void txtCodigoVentaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoVentaKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
        if(!"".equals(txtCodigoVenta.getText())){
            String cod = txtCodigoVenta.getText();
            Inventario.Producto pro = proD.buscarPorCodigo(cod);
            if(pro != null){
                txtDescripcionVenta.setText(pro.getNombre());
                txtPrecioVenta.setText(String.valueOf(pro.getPrecio()));
                txtStockVenta.setText(String.valueOf(pro.getStock()));
                txtCantidadVenta.setText("1"); 
            } else {
                JOptionPane.showMessageDialog(null, "Producto no encontrado");
                txtDescripcionVenta.setText("");
                txtPrecioVenta.setText("");
                txtStockVenta.setText("");
                txtCodigoVenta.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese el Código");
        }
    }
    }//GEN-LAST:event_txtCodigoVentaKeyPressed

    private void txtCantidadVentaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadVentaKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
        String codigo = txtCodigoVenta.getText();
        String nombre = txtDescripcionVenta.getText();
        int stock = Integer.parseInt(txtStockVenta.getText());
        int cantidad;
        double precio;

        try {
            cantidad = Integer.parseInt(txtCantidadVenta.getText());
            precio = Double.parseDouble(txtPrecioVenta.getText());
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Cantidad o precio inválido");
            return;
        }
        if(stock >= cantidad){
        double total = cantidad * precio;
        for(int i= 0; i < modeloTablaVenta.getRowCount();i++){
            if(tableVenta.getValueAt(i,1).equals(txtDescripcionVenta.getText())){
                JOptionPane.showMessageDialog(null,"El producto ya fue registrado");
                return;
            }
        }

        // Agrega a la tabla
        modeloTablaVenta.addRow(new Object[]{
            codigo,
            nombre,
            cantidad,
            precio,
            total
        });
        
        // Limpia los campos
        txtCodigoVenta.setText("");
        txtDescripcionVenta.setText("");
        txtCantidadVenta.setText("");
        txtPrecioVenta.setText("");
        txtStockVenta.setText("");
        txtCodigoVenta.requestFocus();
        totalPagar();
        }else{
            JOptionPane.showMessageDialog(null,"Stock no disponible");
        }
    }

    }//GEN-LAST:event_txtCantidadVentaKeyPressed

