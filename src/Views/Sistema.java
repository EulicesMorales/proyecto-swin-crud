private void inicializarTablaHistorial() {
        DefaultTableModel modeloHistorialVentas;
        modeloHistorialVentas = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
    
    modeloHistorialVentas.addColumn("ID");
    modeloHistorialVentas.addColumn("Fecha");
    modeloHistorialVentas.addColumn("ID Cliente");
    modeloHistorialVentas.addColumn("Productos");
    modeloHistorialVentas.addColumn("Total");
    
    tablaVentas.setModel(modeloHistorialVentas);
}

private void agregarVentaAlHistorial(Ventas.Venta venta) {
    String productosTexto = String.join(", ", venta.getProductos());
    
        modeloHistorialVentas.addRow(new Object[] {
        venta.getId(),
        venta.getFecha(),
        venta.getIdCliente(),
        productosTexto,
        venta.getTotal()
    });
}

